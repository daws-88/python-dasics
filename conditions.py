## if condition
print("if cond")
Marks = 35
if Marks >= 75:
    print("Grade A+")
elif Marks == 35:
    print("Pass")
else:
    print("Fail")

# for loop
print("for loop")
for a in range(0,7,2):   ## for a in range(start,stop,step)
    print(a)

## while loop
print("while loop")
p = 1
while p <= 10:
    print(p)
    p = p + 1


